n=int(input('Enter dictionary size :-'))
dic={}
dictionary1={}
for j in range(n):
    key=int(input('Enter the '+str(j+1)+'key'))
    val=int(input('Enter the '+str(j+1)+'val'))
    dic[key]=val
    if( dic[key] not in list(dictionary1.val())):
        dictionary1[key]=val
